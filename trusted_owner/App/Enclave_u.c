static const struct {
	size_t nr_ocall;
	void * table[9];
} ocall_table_Enclave = {
	9,
	{
		(void*)Enclave_ocall_print_string,
		(void*)Enclave_sgx_oc_cpuidex,
		(void*)Enclave_sgx_thread_wait_untrusted_event_ocall,
		(void*)Enclave_sgx_thread_set_untrusted_event_ocall,
		(void*)Enclave_sgx_thread_setwait_untrusted_events_ocall,
		(void*)Enclave_sgx_thread_set_multiple_untrusted_events_ocall,
		(void*)Enclave_pthread_wait_timeout_ocall,
		(void*)Enclave_pthread_create_ocall,
		(void*)Enclave_pthread_wakeup_ocall,
	}
};
sgx_status_t ecall_deploy_vm(sgx_enclave_id_t eid, int* retval, sev_certs_t certs, measurement_info_t measurement_info, sev_session_buf* out_session_data_buf, sev_cert* out_godh_pubkey_cert)
{
	sgx_status_t status;
	ms_ecall_deploy_vm_t ms;
	ms.ms_certs = certs;
	ms.ms_measurement_info = measurement_info;
	ms.ms_out_session_data_buf = out_session_data_buf;
	ms.ms_out_godh_pubkey_cert = out_godh_pubkey_cert;
	status = sgx_ecall(eid, 0, &ocall_table_Enclave, &ms);
	if (status == SGX_SUCCESS && retval) *retval = ms.ms_retval;
	return status;
}

sgx_status_t ecall_provision_vm(sgx_enclave_id_t eid, int* retval, uint8_t hmac_measurement[32], nonce_128* mnonce, sev_hdr_buf* out_packaged_secret_header, uint8_t out_encrypted_blob[64], measurement_info_t* measurement_info)
{
	sgx_status_t status;
	ms_ecall_provision_vm_t ms;
	ms.ms_hmac_measurement = (uint8_t*)hmac_measurement;
	ms.ms_mnonce = mnonce;
	ms.ms_out_packaged_secret_header = out_packaged_secret_header;
	ms.ms_out_encrypted_blob = (uint8_t*)out_encrypted_blob;
	ms.ms_measurement_info = measurement_info;
	status = sgx_ecall(eid, 1, &ocall_table_Enclave, &ms);
	if (status == SGX_SUCCESS && retval) *retval = ms.ms_retval;
	return status;
}

sgx_status_t ecall_generate_report_for_vm(sgx_enclave_id_t eid, int* retval, uint8_t vm_data[64], hmac_sha_256* vm_data_hmac, sgx_target_info_t* qe_target_info, sgx_report_t* out_sgx_report)
{
	sgx_status_t status;
	ms_ecall_generate_report_for_vm_t ms;
	ms.ms_vm_data = (uint8_t*)vm_data;
	ms.ms_vm_data_hmac = vm_data_hmac;
	ms.ms_qe_target_info = qe_target_info;
	ms.ms_out_sgx_report = out_sgx_report;
	status = sgx_ecall(eid, 2, &ocall_table_Enclave, &ms);
	if (status == SGX_SUCCESS && retval) *retval = ms.ms_retval;
	return status;
}

